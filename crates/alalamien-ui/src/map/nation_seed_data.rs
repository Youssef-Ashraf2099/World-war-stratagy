/// Historical seed data for every nation, calibrated to circa 1939.
///
/// Fields per entry: (iso_a3, gdp_usd, population, legitimacy_0_to_100)
///
/// GDP is approximate 1939 USD (nominal).
/// Population is approximate headcount.
/// Legitimacy reflects internal political stability — high democracies score
/// 70-80, authoritarian-but-cohesive 55-70, colonial / occupied 20-40,
/// war-torn / severely unstable < 35.
///
/// Countries not in this table fall through to `region_defaults()`.

pub struct NationSeedData {
    pub gdp: f64,          // USD
    pub population: u64,   // headcount
    pub legitimacy: f64,   // 0–100
    pub growth_rate: f64,  // annual GDP growth rate (default 0.02)
}

/// Return seed data for `iso_a3`, or a plausible regional default.
pub fn seed_for(iso_a3: &str) -> NationSeedData {
    // (gdp_usd, population, legitimacy, growth_rate)
    let (gdp, pop, leg, gr) = TABLE
        .iter()
        .find(|(code, ..)| *code == iso_a3)
        .map(|(_, g, p, l, gr)| (*g, *p, *l, *gr))
        .unwrap_or_else(|| region_default(iso_a3));

    NationSeedData {
        gdp,
        population: pop,
        legitimacy: leg,
        growth_rate: gr,
    }
}

