/// Fallback region-based defaults for ISO codes not in the table above.
/// These are broad estimates for 1939 colonial / minor territories.
fn region_default(iso_a3: &str) -> (f64, u64, f64, f64) {
    // Cheaply detect region by first letter of ISO code (very rough)
    match iso_a3.chars().next() {
        // Africa: most were colonies — low GDP, high pop, low legitimacy
        Some('A' | 'B' | 'C' | 'D' | 'E' | 'G' | 'K' | 'L' | 'M' | 'N' | 'R' | 'S' | 'T' | 'U' | 'Z') => {
            (80_000_000.0, 1_500_000, 26.0, 0.01)
        }
        // Caribbean / Pacific islands
        Some('F') => (30_000_000.0, 150_000, 30.0, 0.01),
        // Catch-all: small European / Middle-Eastern
        _ => (300_000_000.0, 2_000_000, 45.0, 0.015),
    }
}
